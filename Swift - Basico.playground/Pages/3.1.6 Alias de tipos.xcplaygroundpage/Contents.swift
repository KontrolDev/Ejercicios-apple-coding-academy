///*En Swift, un type alias (alias de tipo) es una forma de proporcionar un nombre alternativo a un tipo existente. Es útil cuando deseas darle a un tipo existente un nombre más descriptivo o más fácil de recordar. Puedes utilizar type alias para crear nombres más cortos y legibles para tipos de datos complejos o para simplificar la sintaxis.

let unaCadena:String = "Swift"

// declaracion de un Typealias
